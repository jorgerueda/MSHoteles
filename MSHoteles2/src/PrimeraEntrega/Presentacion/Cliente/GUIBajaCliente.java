	@Override
	public void actualizar(int id_evento_vista, Object datos) {
		//Borra lo anterior
		jFormattedTextFieldDNI.setText("");
		
		if(id_evento_vista == EventoVista.BAJA_CLIENTE_EXITO){
			JOptionPane.showMessageDialog(null, "Se ha borrado el CLIENTE con ï¿½xito", "Borrar CLIENTE", JOptionPane.INFORMATION_MESSAGE);		
		}	
		else if(id_evento_vista == EventoVista.BAJA_CLIENTE_NO_EXISTE){
			JOptionPane.showMessageDialog(null, "ERROR!! El CLIENTE con ese DNI no existe", "Borrar CLIENTE", JOptionPane.ERROR_MESSAGE);
		}
		else if (id_evento_vista == EventoVista.BAJA_CLIENTE_FALLO){
			JOptionPane.showMessageDialog(null, "ERROR!! Ha ocurrido un error con la BD", "Borrar CLIENTE", JOptionPane.ERROR_MESSAGE);
		}		
	}
